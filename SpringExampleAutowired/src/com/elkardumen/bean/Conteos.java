package com.elkardumen.bean;

public class Conteos {

	
	private String dia;
	
	private int errorCaset=0;
	private int dotacionRe=0;
	private int dotacionPro=0;
	private int casRechazo=0;
	
	public String getDia() {
		return dia;
	}
	public void setDia(String dia) {
		this.dia = dia;
	}
	public int getErrorCaset() {
		return errorCaset;
	}
	public void setErrorCaset(int errorCaset) {
		this.errorCaset = errorCaset;
	}
	public int getDotacionRe() {
		return dotacionRe;
	}
	public void setDotacionRe(int dotacionRe) {
		this.dotacionRe = dotacionRe;
	}
	public int getDotacionPro() {
		return dotacionPro;
	}
	public void setDotacionPro(int dotacionPro) {
		this.dotacionPro = dotacionPro;
	}
	public int getCasRechazo() {
		return casRechazo;
	}
	public void setCasRechazo(int casRechazo) {
		this.casRechazo = casRechazo;
	}
	
	
	
	
}
